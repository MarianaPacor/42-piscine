int	ft_str_is_alpha(char *str)
{
	int i;
	int a;
	int z;

	a = 65;
	z = 90;
	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= a && str[i] <= z) || (str[i] >= 97 && str[i] <= 122)))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
