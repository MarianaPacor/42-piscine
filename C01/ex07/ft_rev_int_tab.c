void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int d;
	int swap;

	i = 0;
	d = size - 1;
	while (i < (size / 2))
	{
		swap = tab[d];
		tab[d] = tab[i];
		tab[i] = swap;
		i++;
		d--;
	}
}
